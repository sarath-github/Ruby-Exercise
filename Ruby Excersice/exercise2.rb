def isSol(i,j)
	$ch[i][j]=1;
	if $maze[i*38+j]=="B"
		$flag1 = 1
		puts("Maze is solvable")
	elsif $flag1 == 0
		if $maze[(i+1)*38+j]!="#" && (i+1)<38 && $ch[(i+1)][j]==0
			isSol((i+1),j);
		end
		if $maze[(i-1)*38+j]!="#" && (i-1)>-1 && $ch[(i-1)][j]==0
			isSol((i-1),j);
		end
		if $maze[(i)*38+(j+1)]!="#" && (j+1)<38 && $ch[i][j+1]==0
			isSol((i),(j+1));
		end
		if $maze[(i)*38+(j-1)]!="#" && (j-1)>-1 && $ch[i][j-1]==0
			isSol((i),(j-1));
		end
	end
end
